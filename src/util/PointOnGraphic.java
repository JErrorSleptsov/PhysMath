package util;

public class PointOnGraphic {
    double X;
    double Y;

    public PointOnGraphic() {

    }

    public PointOnGraphic(double x, double y) {
        X = x;
        Y = y;
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }

    public double getY() {
        return Y;
    }

    public void setY(double y) {
        Y = y;
    }
}
